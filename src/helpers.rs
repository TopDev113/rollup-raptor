pub fn u64_to_u8_array(x: u64) -> [u8; 8] {
    [
        ((x / 0x100000000000000) % 256) as u8,
        ((x / 0x1000000000000) % 256) as u8,
        ((x / 0x10000000000) % 256) as u8,
        ((x / 0x100000000) % 256) as u8,
        ((x / 0x1000000) % 256) as u8,
        ((x / 0x10000) % 256) as u8,
        ((x / 0x100) % 256) as u8,
        (x % 256) as u8,
    ]
}